    /***
     * Sincero es el nombre de la clase publica que hace que el resultaddo si es entero no tenga decimales
     **/
    private void jButton28ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton28ActionPerformed
        this.numero2=Float.parseFloat(this.casilla.getText());
        
        switch(this.operador){
            case "+" :this.casilla.setText(sincero(this.numero1+this.numero2));
            break;
            case "-" :this.casilla.setText(sincero(this.numero1-this.numero2));
            break;
            case "*" :this.casilla.setText(sincero(this.numero1*this.numero2));
            break;
            case "/" :if(this.numero2==0) {this.casilla.setText("No se puede dividir");}
            else{
                this.casilla.setText(sincero(this.numero1/this.numero2));
            break;
            }
        }
    }//GEN-LAST:event_jButton28ActionPerformed

