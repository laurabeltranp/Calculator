    /***
     * Funci√≥n que sirve para eliminar el 0 automatico del resultado
     * @param resultado
     * @return 
     */
    public String sincero(float resultado){
        String devuelve="";
        
        devuelve=Float.toString(resultado);
        
        if(resultado%1==0){
            devuelve=devuelve.substring(0, devuelve.length()-2);
            
        }
        
        return devuelve;
    }
    
